    const l = selLiga ? selLiga.value : ""
    const a = selActivo ? selActivo.value : ""

    if (p && p !== "") {
      filtrarPorPais(p)
    }
    if (l && l !== "") {
      filtrarPorLiga(l)
    }

    if (a === "1") {
      filtrarPorActivo(true)
    } else if (a === "0") {
      filtrarPorActivo(false)
    }

