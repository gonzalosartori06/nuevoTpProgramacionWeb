function formatearNombreTitulo(str) {
  const limpio = (str || "").trim().replace(/\s+/g, " ")
  if (limpio === "") return ""
  return limpio
    .split(" ")
    .map((w) => {
      if (w.length === 0) return w
      const first = w.charAt(0).toUpperCase()
      const rest = w.slice(1).toLowerCase()
      return first + rest
    })
    .join(" ")
}

